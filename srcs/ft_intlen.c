#include "../includes/libft.h"

int	ft_intlen(int nb)
{
	int i;

	i = 0;
	if (!nb)
		i++;
	if (nb < 0)
		i++;
	while (nb)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
